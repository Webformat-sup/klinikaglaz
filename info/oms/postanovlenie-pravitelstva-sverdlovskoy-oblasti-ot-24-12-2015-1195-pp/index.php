<p>
	 38. В медицинских организациях, оказывающих специализированную медицинскую помощь в стационарных условиях, ведется лист ожидания оказания специализированной медицинской помощи в плановой форме и осуществляется информирование граждан в доступной форме, в том числе с использованием информационно-телекоммуникационной сети Интернет, о сроках ожидания оказания специализированной медицинской помощи с учетом требования законодательства Российской Федерации о персональных данных.
</p>
<p>
	 39. Высокотехнологичная медицинская помощь жителям Свердловской области оказывается в медицинских организациях, подведомственных федеральным органам исполнительной власти, и медицинских организациях Свердловской области, имеющих лицензию на оказание высокотехнологичной медицинской помощи. Решение о необходимости оказания пациенту высокотехнологичной медицинской помощи (далее — ВМП) в медицинских организациях, подведомственных федеральным органам исполнительной власти, принимает Комиссия Министерства здравоохранения Свердловской области по отбору больных на ВМП (далее — Комиссия). Решение о необходимости оказания пациенту ВМП в медицинских организациях Свердловской области принимает Комиссия по отбору больных на ВМП, которая создается в медицинских организациях, оказывающих ВМП. Показания для направления на ВМП определяет главный внештатный специалист Министерства здравоохранения Свердловской области по профилю заболевания (при направлении в медицинские организации, подведомственные федеральным органам исполнительной власти) или врач-специалист профильного консультативного приема с учетом предварительно проведенных диагностических исследований. Заключение главного внештатного специалиста Министерства здравоохранения Свердловской области по профилю заболевания или врача-специалиста профильного консультативного приема предъявляется в медицинскую организацию по месту жительства для оформления необходимого пакета медицинских документов и предоставления их в Комиссию. Решение Комиссии оформляется в виде протокола с заключением о необходимости лечения, виде медицинского вмешательства и форме оказания (экстренная, плановая или неотложная).
</p>
<p>
	 Плановая ВМП оказывается в соответствии с листом ожидания. О дате госпитализации для оказания ВМП пациент информируется медицинской организацией Свердловской области, оказывающей ВМП, или Министерством здравоохранения Свердловской области (территориальными отделами здравоохранения по управленческим округам).
</p>
<p>
	 40. Время ожидания плановой госпитализации в медицинских организациях для оказания специализированной медицинской помощи, за исключением ВМП, допускается в пределах, не превышающих 30 дней с момента выдачи лечащим врачом направления на госпитализацию (при условии обращения пациента за госпитализацией в рекомендуемые лечащим врачом сроки), а также в зависимости от состояния пациента и характера течения заболевания. Время ожидания плановой госпитализации для получения ВМП по разным профилям определяется исходя из потребности граждан в тех или иных видах медицинской помощи, ресурсных возможностей медицинской организации и наличия очередности.
</p>
<p>
	 41. Условия госпитализации в медицинские организации:
</p>
<p>
	 1) обязательным условием является наличие направления на плановую госпитализацию и данных догоспитального обследования;
</p>
<p>
	 2) госпитализация пациента в медицинскую организацию по экстренным показаниям должна осуществляться в максимально короткие сроки, время нахождения больного в приемном покое при плановой госпитализации не должно превышать 3 часов;
