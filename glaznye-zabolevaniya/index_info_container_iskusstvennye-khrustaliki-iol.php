			<div class="header-title">
 <b>Что такое искусственный хрусталик?</b>
			</div>
			<div class="header-icon">
			</div>
		</div>
		<div class="content">
			<p>
				 Искусственный хрусталик (интраокулярная линза) - это имплантат из биосовместимого материала, который устанавливается на место естественного хрусталика при операции по поводу катаракты или при оперативном лечении высоких степеней близорукости, дальнозоркости или астигматизма.
			</p>
		</div>
	</div>
</div>
<div class="collapse-container snippets">
	<div class="collapse-item" data-id="" data-status="close">
		<div class="header">
			<div class="header-title">
 <b>Как устроен искусственный хрусталик?</b>
			</div>
			<div class="header-icon">
			</div>
		</div>
		<div class="content">
			<p>
				 Искусственный хрусталик состоит из двух элементов - оптического и опорного. Оптическая часть - это прозрачная линза, за счет которой достигается правильная фокусировка световых лучей и обеспечивается хорошее зрение, а опорная (гаптическая) часть состоит из двух фиксаторов и позволяет закрепить ИОЛ в капсуле природного хрусталика.
			</p>
		</div>
	</div>
</div>
<div class="collapse-container snippets">
	<div class="collapse-item" data-id="" data-status="close">
		<div class="header">
			<div class="header-title">
 <b>Когда имплантируется ИОЛ?</b>
			</div>
			<div class="header-icon">
			</div>
		</div>
		<div class="content">
			<p>
				 Интраокулярная линза имплантируется вместо естественного хрусталика: при лечении катаракты, когда потерявший прозрачной природный хрусталик заменяют на искусственный; для коррекции зрения при высоких степенях близорукости, дальнозоркости или астигматизма (рефракционная замена хрусталика).
			</p>
		</div>
	</div>
</div>
