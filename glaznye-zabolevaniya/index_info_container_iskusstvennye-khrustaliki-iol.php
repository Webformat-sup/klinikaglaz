			Показания к имплантации ИОЛ:
		</div>
		<div class="info-content">
			<div class="info-content">
				<div class="bl bl-1">
					 Имплантация интраокулярной линзы показана пациентам, которые имеют следующие офтальмологические заболевания:
				</div>
				<div class="bl bl-2">
					<ul>
						<li>Миопия (близорукость) высокой степени (до -25 диоптрий);</li>
						<li>Дальнозоркость (гиперметропия) высокой степени (до +20 диоптрий);</li>
						<li>Астигматизм высокой степени (до 6 диоптрий);</li>
						<li>Изменения в структуре роговицы глаза (помутнение, рубцевание);</li>
						<li>Катаракта и глаукома.</li>
					</ul>
				</div>
			</div>
		</div>
	</div>
	<div class="info-item">
		<div class="info-title">
			Противопоказания к имплантации ИОЛ:
		</div>
		<div class="info-content">
			<div class="info-content">
				<div class="bl bl-1">
					 Противопоказаниями к имплантации ИОЛ являются увеины (воспаления сосудистой оболочки глаза), так как проведение данной операции вызывает усиление воспалений, что может быть губительным для глаза. Также имплантация ИОЛ противопоказана при следующих нарушениях глаза:
				</div>
				<div class="bl bl-2">
					<ul>
						<li>Атрофия зрительного нерва, дистрофия сетчатки глаза, любые воспалительные процессы на глазном днем являются полном противопокаанием к имплантации линзы;</li>
						<li>Хронические воспаления сосудистой оболочки глаза (увеиты);</li>
						<li>Подвывих хрусталика;</li>
						<li>Предшествующие глазные операции (на сетчатке и стекловидном теле).</li>
					</ul>
				</div>
			</div>
