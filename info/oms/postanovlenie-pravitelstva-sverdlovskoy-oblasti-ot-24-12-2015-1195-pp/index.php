<p align="center">
	 Глава 1. ОБЩИЕ ПОЛОЖЕНИЯ
</p>
<p>
</p>
<p>
	 1. Настоящие Порядок и условия предоставления бесплатной медицинской помощи регламентируют принципы организации населению бесплатной медицинской помощи и условия ее предоставления в медицинских организациях, участвующих в реализации Территориальной программы государственных гарантий бесплатного оказания гражданам медицинской помощи в Свердловской области на 2016 год (далее — Программа).
</p>
<p>
	 2. Оказание медицинской помощи осуществляется в медицинских организациях при наличии у них лицензии на медицинскую деятельность, выданной в порядке, установленном законодательством Российской Федерации.
</p>
<p>
	 3. Медицинская помощь гражданам может оказываться в следующих условиях:
</p>
<p>
	 1) вне медицинской организации (по месту вызова бригады скорой, в том числе скорой специализированной, медицинской помощи, а также в транспортном средстве при медицинской эвакуации);
</p>
<p>
	 2) амбулаторно (в условиях, не предусматривающих круглосуточного медицинского наблюдения и лечения), в том числе на дому при вызове медицинского работника;
</p>
<p>
	 3) в дневном стационаре (в условиях, предусматривающих медицинское наблюдение и лечение в дневное время, но не требующих круглосуточного медицинского наблюдения и лечения);
</p>
<p>
	 4) стационарно (в условиях медицинских организаций или в их соответствующих структурных подразделениях, обеспечивающих круглосуточное медицинское наблюдение и лечение в случаях, требующих применения интенсивных методов диагностики и лечения и (или) изоляции, в том числе по эпидемиологическим показаниям).
</p>
<p>
	 4. При оказании медицинской помощи в рамках Программы граждане имеют право на выбор медицинской организации в&nbsp;<a href="consultantplus://offline/ref=DA5C709B786DAE47A934F0B81BC74FAFC163D6BCC69CAF030C670FB39B057BEA20C6BF757A020D34sEu1G%20/%20%D0%9F%D1%80%D0%B8%D0%BA%D0%B0%D0%B7%20%D0%9C%D0%B8%D0%BD%D0%B7%D0%B4%D1%80%D0%B0%D0%B2%D1%81%D0%BE%D1%86%D1%80%D0%B0%D0%B7%D0%B2%D0%B8%D1%82%D0%B8%D1%8F%20%D0%A0%D0%BE%D1%81%D1%81%D0%B8%D0%B8%20%D0%BE%D1%82%2026.04.2012%20N%20406%D0%BD%20/%D0%9E%D0%B1%20%D1%83%D1%82%D0%B2%D0%B5%D1%80%D0%B6%D0%B4%D0%B5%D0%BD%D0%B8%D0%B8%20%D0%9F%D0%BE%D1%80%D1%8F%D0%B4%D0%BA%D0%B0%20%D0%B2%D1%8B%D0%B1%D0%BE%D1%80%D0%B0%20%D0%B3%D1%80%D0%B0%D0%B6%D0%B4%D0%B0%D0%BD%D0%B8%D0%BD%D0%BE%D0%BC%20%D0%BC%D0%B5%D0%B4%D0%B8%D1%86%D0%B8%D0%BD%D1%81%D0%BA%D0%BE%D0%B9%20%D0%BE%D1%80%D0%B3%D0%B0%D0%BD%D0%B8%D0%B7%D0%B0%D1%86%D0%B8%D0%B8%20%D0%BF%D1%80%D0%B8%20%D0%BE%D0%BA%D0%B0%D0%B7%D0%B0%D0%BD%D0%B8%D0%B8%20%D0%B5%D0%BC%D1%83%20%D0%BC%D0%B5%D0%B4%D0%B8%D1%86%D0%B8%D0%BD%D1%81%D0%BA%D0%BE%D0%B9%20%D0%BF%D0%BE%D0%BC%D0%BE%D1%89%D0%B8%20%D0%B2%20%D1%80%D0%B0%D0%BC%D0%BA%D0%B0%D1%85%20%D0%BF%D1%80%D0%BE%D0%B3%D1%80%D0%B0%D0%BC%D0%BC%D1%8B%20%D0%B3%D0%BE%D1%81%D1%83%D0%B4%D0%B0%D1%80%D1%81%D1%82%D0%B2%D0%B5%D0%BD%D0%BD%D1%8B%D1%85%20%D0%B3%D0%B0%D1%80%D0%B0%D0%BD%D1%82%D0%B8%D0%B9%20%D0%B1%D0%B5%D1%81%D0%BF%D0%BB%D0%B0%D1%82%D0%BD%D0%BE%D0%B3%D0%BE%20%D0%BE%D0%BA%D0%B0%D0%B7%D0%B0%D0%BD%D0%B8%D1%8F%20%D0%B3%D1%80%D0%B0%D0%B6%D0%B4%D0%B0%D0%BD%D0%B0%D0%BC%20%D0%BC%D0%B5%D0%B4%D0%B8%D1%86%D0%B8%D0%BD%D1%81%D0%BA%D0%BE%D0%B9%20%D0%BF%D0%BE%D0%BC%D0%BE%D1%89%D0%B8/%20(%D0%97%D0%B0%D1%80%D0%B5%D0%B3%D0%B8%D1%81%D1%82%D1%80%D0%B8%D1%80%D0%BE%D0%B2%D0%B0%D0%BD%D0%BE%20%D0%B2%20%D0%9C%D0%B8%D0%BD%D1%8E%D1%81%D1%82%D0%B5%20%D0%A0%D0%BE%D1%81%D1%81%D0%B8%D0%B8%2021.05.2012%20N%2024278)%0a%D0%9A%D0%BE%D0%BD%D1%81%D1%83%D0%BB%D1%8C%D1%82%D0%B0%D0%BD%D1%82%D0%9F%D0%BB%D1%8E%D1%81">порядке</a>, утвержденном Приказом Министерства здравоохранения и социального развития Росс