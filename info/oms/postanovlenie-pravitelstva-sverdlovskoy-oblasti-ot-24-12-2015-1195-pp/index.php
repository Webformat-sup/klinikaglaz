</p>
<p>
	 5. Порядок выбора гражданином медицинской организации (за исключением случаев оказания скорой медицинской помощи) за пределами территории субъекта Российской Федерации, в котором проживает гражданин, при оказании ему медицинской помощи в рамках Программы регулируется нормативными правовыми актами Министерства здравоохранения Российской Федерации.
</p>
<p>
	 6. При оказании медицинской помощи необходимо добровольное информированное согласие (отказ) пациента на медицинское вмешательство, которое оформляется в порядке, установленном нормативными правовыми актами Российской Федерации.
</p>
<p>
	 7. Если медицинской организацией не может быть оказана необходимая медицинская помощь, медицинская организация обеспечивает перевод гражданина в другую медицинскую организацию, в которой предусмотрено оказание необходимой медицинской помощи.
</p>
<p>
	 8. Медицинские организации, участвующие в реализации Программы, размещают информацию о гарантиях оказания гражданам бесплатной медицинской помощи в общедоступном месте и на официальном сайте медицинской организации в информационно-телекоммуникационной сети Интернет в соответствии с Федеральным&nbsp;<a href="consultantplus://offline/ref=DA5C709B786DAE47A934F0B81BC74FAFC169D6BBCD9AAF030C670FB39Bs0u5G%20/%20%D0%A4%D0%B5%D0%B4%D0%B5%D1%80%D0%B0%D0%BB%D1%8C%D0%BD%D1%8B%D0%B9%20%D0%B7%D0%B0%D0%BA%D0%BE%D0%BD%20%D0%BE%D1%82%2029.11.2010%20N%20326-%D0%A4%D0%97%20(%D1%80%D0%B5%D0%B4.%20%D0%BE%D1%82%2030.12.2015)%20/%D0%9E%D0%B1%20%D0%BE%D0%B1%D1%8F%D0%B7%D0%B0%D1%82%D0%B5%D0%BB%D1%8C%D0%BD%D0%BE%D0%BC%20%D0%BC%D0%B5%D0%B4%D0%B8%D1%86%D0%B8%D0%BD%D1%81%D0%BA%D0%BE%D0%BC%20%D1%81%D1%82%D1%80%D0%B0%D1%85%D0%BE%D0%B2%D0%B0%D0%BD%D0%B8%D0%B8%20%D0%B2%20%D0%A0%D0%BE%D1%81%D1%81%D0%B8%D0%B9%D1%81%D0%BA%D0%BE%D0%B9%20%D0%A4%D0%B5%D0%B4%D0%B5%D1%80%D0%B0%D1%86%D0%B8%D0%B8/%20(%D1%81%20%D0%B8%D0%B7%D0%BC.%20%D0%B8%20%D0%B4%D0%BE%D0%BF.,%20%D0%B2%D1%81%D1%82%D1%83%D0%BF.%20%D0%B2%20%D1%81%D0%B8%D0%BB%D1%83%20%D1%81%2001.01.2016)%0a%D0%9A%D0%BE%D0%BD%D1%81%D1%83%D0%BB%D1%8C%D1%82%D0%B0%D0%BD%D1%82%D0%9F%D0%BB%D1%8E%D1%81">законом</a>&nbsp;от 29 ноября 2010 года N 326-ФЗ «Об обязательном медицинском страховании в Российской Федерации».
