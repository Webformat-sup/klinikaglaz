<p align="center">
	 МЕДИЦИНСКОЙ ПОМОЩИ
</p>
<p>
</p>
<p>
	 34. Специализированная, в том числе высокотехнологичная, медицинская помощь оказывается в стационарных условиях и условиях дневного стационара.
</p>
<p>
	 35. Госпитализация в условиях обеспечения круглосуточного медицинского наблюдения и лечения (стационарно) осуществляется по направлению лечащего врача или врача-специалиста амбулаторно-поликлинической медицинской организации в соответствии с медицинскими показаниями, требующими госпитального режима, проведения интенсивных методов лечения и круглосуточного наблюдения врача, скорой медицинской помощью по срочным медицинским показаниям, а также при самостоятельном обращении пациента для оказания экстренной медицинской помощи при наличии показаний к госпитализации.
</p>
<p>
	 36. Специализированная медицинская помощь в условиях обеспечения круглосуточного медицинского наблюдения и лечения (стационарно) организуется в соответствии с порядками оказания медицинской помощи, утверждаемыми нормативными правовыми актами уполномоченного федерального органа исполнительной власти.
</p>
<p>
	 37. Оказание плановой специализированной медицинской помощи в условиях стационара гражданам из других муниципальных образований, расположенных на территории Свердловской области, осуществляется бесплатно по направлению медицинской организации по месту жительства пациента в соответствии с порядками оказания медицинской помощи и маршрутизации пациентов по профилям медицинской помощи, установленными нормативными документами Российской Федерации и Свердловской области, с учетом установленного Программой периода ожидания.
