<p>
	 9. Медицинские организации, участвующие в реализации Программы, Министерство здравоохранения Свердловской области, Территориальный фонд обязательного медицинского страхования Свердловской области рассматривают обращения граждан по вопросам оказания бесплатной медицинской помощи в порядке, установленном Федеральным&nbsp;<a href="consultantplus://offline/ref=DA5C709B786DAE47A934F0B81BC74FAFC168DEBDC195AF030C670FB39Bs0u5G%20/%20%D0%A4%D0%B5%D0%B4%D0%B5%D1%80%D0%B0%D0%BB%D1%8C%D0%BD%D1%8B%D0%B9%20%D0%B7%D0%B0%D0%BA%D0%BE%D0%BD%20%D0%BE%D1%82%2002.05.2006%20N%2059-%D0%A4%D0%97%20(%D1%80%D0%B5%D0%B4.%20%D0%BE%D1%82%2003.11.2015)%20/%D0%9E%20%D0%BF%D0%BE%D1%80%D1%8F%D0%B4%D0%BA%D0%B5%20%D1%80%D0%B0%D1%81%D1%81%D0%BC%D0%BE%D1%82%D1%80%D0%B5%D0%BD%D0%B8%D1%8F%20%D0%BE%D0%B1%D1%80%D0%B0%D1%89%D0%B5%D0%BD%D0%B8%D0%B9%20%D0%B3%D1%80%D0%B0%D0%B6%D0%B4%D0%B0%D0%BD%20%D0%A0%D0%BE%D1%81%D1%81%D0%B8%D0%B9%D1%81%D0%BA%D0%BE%D0%B9%20%D0%A4%D0%B5%D0%B4%D0%B5%D1%80%D0%B0%D1%86%D0%B8%D0%B8/%0a%D0%9A%D0%BE%D0%BD%D1%81%D1%83%D0%BB%D1%8C%D1%82%D0%B0%D0%BD%D1%82%D0%9F%D0%BB%D1%8E%D1%81">законом</a>&nbsp;от 02 мая 2006 года N 59-ФЗ «О порядке рассмотрения обращений граждан Российской Федерации».
